package jet.com.modules.menu;

public class MenuSchema {


    private String path, module;
    
    public MenuSchema(){}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getModule() {
		return module;
	}

	public void setModule(String module) {
		this.module = module;
	}

    
    
}
